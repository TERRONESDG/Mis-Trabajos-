# definicion de la funcion
def saludar(nombre, edad): # nombre es un parametro
    print(f'\n¡Hola, {nombre}!')
    print(f'Tienes {edad} años.')
    print('¡Espero que te encuentres bien!\n')
    
# Llamada a la funcion
saludar('Edinguer', 26) # Edinguer y 26 son  un argumento
saludar('Chuyito', 18) # Chuyito y 18 son  un argumento
saludar('terricola', 23) # terricola y 23 son  un argumento
