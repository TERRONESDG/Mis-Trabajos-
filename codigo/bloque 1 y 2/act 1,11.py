#operadores de comparacion == , !=, >=, <=, >, <

print("Operador de IGUAL (==)")
print(5==7) #respuesta False
print(5==5) #respuesta True
print(5.0==5) #respuesta True
print("\n") #salto de linea y retorno de carro


print ("Operador DIFERENTE O IGUAL (!=)")
print(8!=7) #respuesta True
print(8!=5) #respuesta True
print(7.0!=5) #respuesta True
print("\n") #salto de linea y retorno de carro

print ("Operador MAYOR O IGUAL (>=)")
print(8>=7) #respuesta true
print(8>=5) #respuesta true
print(7.0>=5) #respuesta true
print("\n") #salto de linea y retorno de carro

print ("Operador MENOR O IGUAL (<=)")
print(8<=7) #respuesta false
print(8<=5) #respuesta false
print(7.0<=5) #respuesta false
print("\n") #salto de linea y retorno de carro

print ("Operador MAYOR (>)")
print(8>7) #respuesta True
print(8>5) #respuesta True
print(7.0>5) #respuesta True
print("\n") #salto de linea y retorno de carro

print ("Operador MENOR (<)")
print(8<7) #respuesta false
print(8<5) #respuesta false
print(7.0<5) #respuesta false
print("\n") #salto de linea y retorno de carro

